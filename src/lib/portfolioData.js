export function getProjectsByShootLocation(location, minCount = 4) {
  const matched = portfolioProjects.filter(
    (project) => project.shootLocations?.includes(location)
  );
  if (matched.length >= minCount) return matched;

  const featured = portfolioProjects.filter((project) => project.featured);
  const combined = [...matched];
  for (const project of featured) {
    if (!combined.some((item) => item.slug === project.slug)) {
      combined.push(project);
    }
    if (combined.length >= minCount) break;
  }
  return combined.length > 0 ? combined : portfolioProjects.slice(0, minCount);
}

export function getPortfolioProject(slug) {
  const decoded = decodeURIComponent(slug);
  return portfolioProjects.find(
    (project) => project.slug === slug || project.slug === decoded
  );
}

export function getFeaturedProjects(limit = 3) {
  const featured = portfolioProjects.filter((project) => project.featured);
  return featured.length > 0 ? featured.slice(0, limit) : portfolioProjects.slice(0, limit);
}
